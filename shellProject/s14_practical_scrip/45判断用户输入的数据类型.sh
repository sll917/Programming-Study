echo '(1)------------------------------------------'
# 判断用户输入的数据类型(字母、数字或其他)
read -p "请输入一个字符:" KEY
case "$KEY" in
  [a-z]|[A-Z])
    echo "你输入的是字母"
    ;;
  [0-9])
    echo "你输入的是数字"
    ;;
  *)
    echo "你输入的是空格、功能键或其他控制字符"
esac
